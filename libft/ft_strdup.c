#include "libft.h"

char	*ft_strdup(const char *s)
{
	unsigned int	i;
	char			*c;

	i = 0;
	c = malloc(sizeof(char) * (ft_strlen((char *)s) + 1));
	while (s[i] != '\0')
	{
		c[i] = s[i];
		i++;
	}
	c[i] = '\0';
	return (c);
}
